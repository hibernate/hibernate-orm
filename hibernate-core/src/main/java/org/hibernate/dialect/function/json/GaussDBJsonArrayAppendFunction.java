package org.hibernate.dialect.function.json;

import java.util.List;

import org.hibernate.metamodel.mapping.JdbcMappingContainer;
import org.hibernate.metamodel.model.domain.ReturnableType;
import org.hibernate.sql.ast.SqlAstTranslator;
import org.hibernate.sql.ast.spi.SqlAppender;
import org.hibernate.sql.ast.tree.SqlAstNode;
import org.hibernate.sql.ast.tree.expression.Expression;
import org.hibernate.type.spi.TypeConfiguration;

/**
 * GaussDB json_array_append function.
 *
 * @author liubao
 *
 * Notes: Original code of this class is based on PostgreSQLJsonArrayAppendFunction.
 */
public class GaussDBJsonArrayAppendFunction extends AbstractJsonArrayAppendFunction {

	private final boolean supportsLax;

	public GaussDBJsonArrayAppendFunction(boolean supportsLax, TypeConfiguration typeConfiguration) {
		super( typeConfiguration );
		this.supportsLax = supportsLax;
	}

	@Override
	public void render(
			SqlAppender sqlAppender,
			List<? extends SqlAstNode> arguments,
			ReturnableType<?> returnType,
			SqlAstTranslator<?> translator) {


	}

	private static boolean isJsonType(Expression expression) {
		final JdbcMappingContainer expressionType = expression.getExpressionType();
		return expressionType != null && expressionType.getSingleJdbcMapping().getJdbcType().isJson();
	}
}
